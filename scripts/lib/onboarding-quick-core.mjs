// Helpers for onboarding-quick.mjs (olympiad topic blocks).

export function defaultOlympiadTopics(subject) {
  const blocks = {
    math: ["Алгебра", "Геометрия", "Комбинаторика", "Теория чисел"],
    математика: ["Алгебра", "Геометрия", "Комбинаторика", "Теория чисел"],
    physics: ["Механика", "Термодинамика", "Электродинамика", "Оптика"],
    физика: ["Механика", "Термодинамика", "Электродинамика", "Оптика"],
    informatics: ["Структуры данных", "Динамическое программирование", "Графы"],
    информатика: ["Структуры данных", "Динамическое программирование", "Графы"],
  };
  const key = String(subject || "").toLowerCase();
  return blocks[key] || [`Подготовка: ${subject}`];
}
